package assignment1;

public class Rectangle {
	private int width;
	private int height;
	
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int ConputeArea() {
		return width * height;
	}
	
}
